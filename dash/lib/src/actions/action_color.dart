/// Color variants for actions.
///
/// Actions can be styled with different colors to convey their purpose:
/// - [primary] - Default color for primary actions (lime/green)
/// - [secondary] - Secondary/neutral actions (gray)
/// - [danger] - Destructive actions like delete (red)
/// - [warning] - Warning actions (amber/yellow)
/// - [success] - Success/positive actions (green)
/// - [info] - Informational actions (blue)
enum ActionColor { primary, secondary, danger, warning, success, info }
